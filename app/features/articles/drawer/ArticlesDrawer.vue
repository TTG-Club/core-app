<script setup lang="ts">
  import { ArticlesBody } from '../body';
  import { UiDrawer } from '~ui/drawer';

  import type { ArticlesDetailResponse } from '../types';

  const { url } = defineProps<{
    url: string;
  }>();

  defineEmits<{
    (e: 'close'): void;
  }>();

  const { data: detail, status } = await useAsyncData(
    computed(() => `articles-${url}`),
    () => $fetch<ArticlesDetailResponse>(`/api/v2/articles/${url}`),
    {
      server: false,
      immediate: true,
    },
  );

  const isLoading = computed(() => status.value === 'pending');
  const isError = computed(() => status.value === 'error');
  const urlForCopy = computed(() => `${getOrigin()}/articles/${url}`);
  const editUrl = computed(() => `/workshop/articles/${url}`);
</script>

<template>
  <UiDrawer
    :title="detail?.name"
    :source="detail?.source"
    :date-time="detail?.updatedAt"
    :url="urlForCopy"
    :edit-url="editUrl"
    :is-loading
    :is-error
    copy-title
    @close="$emit('close')"
  >
    <ArticlesBody
      v-if="detail"
      :articles="detail"
    />
  </UiDrawer>
</template>
