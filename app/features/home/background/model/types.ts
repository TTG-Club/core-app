/**
 * Интерфейс для описания частицы анимации
 */
export interface Particle {
  /**
   * Координата X частицы на canvas
   */
  x: number;

  /**
   * Координата Y частицы на canvas
   */
  y: number;

  /**
   * Размер частицы в пикселях
   */
  size: number;

  /**
   * Скорость движения по оси Y (пиксели за кадр)
   */
  speedY: number;

  /**
   * Прозрачность частицы (0-1)
   */
  opacity: number;
}
